public class Variables {
    public static void main(String[] args) {
        // VARIABLES
        int age = 92;
        double price = 100.40;
        boolean available = true;
        char gender;
        gender = 'M';
        // Access to Variables
        System.out.println(age);
        System.out.println(price);
        System.out.println(available);
        System.out.println("gender = " + gender);
    }

}
